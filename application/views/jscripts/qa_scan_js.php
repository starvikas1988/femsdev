<script type="text/javascript">
	function scan_calc(){
		var score = 0;
		var sub_score=0;
		var scoreable = 0;
		var quality_score_percent = 0;
		var nameVal="";
		var	store="";

		$('.scanVal').each(function(index,element){
			var score_type = $(element).val();
					
			
			var nameVal = $.trim($(element).attr('name'));		
				
			var weightage = parseFloat($(element).children("option:selected").attr('scan_val'));
			var weightage_possible = parseFloat($(element).children("option:selected").attr('scan_max'));
			var final_total=$.trim($(this).data('id'));
			score = score + weightage;
			sub_score=sub_score + weightage;
			scoreable = scoreable + weightage_possible;
			//$("#score_"+nameVal).html(weightage);
			$("#score_"+final_total).html(sub_score);
			
			//console.log(score);
			if(final_total != store){
				sub_score = weightage;
				$("#score_"+final_total).html(sub_score);
			}
			store = final_total;

		});

		quality_score_percent = ((score*100)/scoreable).toFixed(2);
		
		$('#scanEarned').val(score);
		$('#scanPossible').val(scoreable);
		
		if(!isNaN(quality_score_percent)){
			$('#scanOverallScore').val(quality_score_percent+'%');
		}else{
			$('#scanOverallScore').val('0.00'+'%');
		}
	}

	$(document).on('change','.scanVal',function(){
		scan_calc();
	});
	scan_calc();
</script>


