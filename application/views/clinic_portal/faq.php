					<h4 style="color: #2777da;"><b>SSS SICKNESS BENEFIT APPLICATION</b></h4><br>
		
					<span style='font-size:13px; font-weight:bold; '>Where to submit the required documents?</span>
					<br>
					<span>Place all your papers in a brown envelope with your name labeled on it and drop it in our COMPBEN 
					dropbox located at Site1 frontdesk area anytime.</span><br><br>
					
					<span style='font-size:13px; font-weight:bold; '>How many days in a year can a member be granted sickness benefit?</span><br>
					<span>The sickness benefit shall not be paid for more than 240 days on account of the same illness. If the 
					sickness or injury persists after 240 days, the claim will be considered a disability claim.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>What is the prescriptive period in the filing of sickness benefit claim?</span><br>
					<span>*Home confinement- SBA Form must be submitted to SSS within five (5) calendar days after the start date of confinement.</span><br>
					<span>*Hospital confinement- SBA Form must be submitted to SSS within one (1) year from the date of hospital discharge.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>What are the implications of late filing?</span><br>
					<span>Failure to observe the rule on notification shall be a ground for the reduction or denial of the sickness claim application. If the member notifies the SSS beyond the prescribed five-day period, the confinement shall be deemed to have started not earlier than the fifth (5th) day immediately preceding the date of notification.</span><br><br>
					<div style="width: 63%;border: 1px solid;">
						
						<p style="padding: 10px 15px 0px 15px;">EXAMPLE: The member gets sick and was confined at home from March 1 to 31, 2019 for a total of 31 days.<br>The notification was done on March 8, 2019 (two days late).<br>Notification should have been made between March 2 to 6, 2019 or within five (5) calendar days after the start date of confinement.<br>Hence, the compensable period will be from March 3 to 31, 2019 only for a total of 29 days</p>
							
					</div>

					<br>

					<span style='font-size:13px; font-weight:bold; '>What if the last day of filing falls on a weekend or a holiday?</span><br>

					<span>Whenever the last day to notify the SSS of a member’s sickness or injury falls on a Saturday, Sunday, or holiday, the sickness benefit application may be filed on the immediately succeeding working day without the penalty of delay. Holiday refers to the legal, regular, or special non-working day declared by the government, both national and local.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>What is the disbursement schedule of sickness benefit payment?</span><br>

					<span>Estimated turnaround time of this application is 15 working days from receipt of the complete documents. Once approved by SSS, COMPBEN team reverts to the employee advising the schedule of nearest payout via ATM whichever cut-off period the SSS approval falls in. COMPBEN team may also touch base with the employee for the procurement of additional requirements, if there’s any, depending on the advice of the SSS verification personnel.</span><br><br><br>

					
					<h4 style="color: #2777da;"><b>SSS MATERNITY BENEFIT APPLICATION</b></h4><br>
					<span style='font-size:13px; font-weight:bold; '>Where to submit the required documents?</span><br>
					<span>Place all your papers in a brown envelope with your name labeled on it and drop it in our COMPBEN dropbox located at Site1 frontdesk area anytime.</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>When should I file my maternity notification?</span><br>
					<span>You need to notify our health team of your pregnancy at least 60 days from date of conception (not later than 2 months AOG) so they could discuss to you the MAT1 requirements in details.</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>When should I start my Maternity leave?</span><br>
					<span>Maternity leave starts 2 weeks before your expected due date per DOLE guidelines. Prior to that, our health team will revert to you to discuss the inclusive duration of your MAT leave, MAT2 requirements and schedule of first half MAT benefit in details.</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>Can I file for an early paid Maternity leave?</span><br>
					<span>Yes, provided that you have a written certification duly issued by your attending OBgyne specialist and other valid medical documents to support your claim, subject for approval from the health team and Ops team. Maximum of 1 month before the expected delivery date is the allowable early maternity leave provision. More than that, we may tag the employee as LOA (leave of absence).</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>When is the first half MAT benefit be credited through payroll ATM?</span><br>
					<span>This will be credited on the nearest payday from the start date of your MAT leave as set by the health team.</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>When is the second half MAT benefit be credited through payroll ATM?</span><br>
					<span>This will be credited on the nearest payday whichever cut-off period the accomplishment of MAT2 requirements falls in.</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>What to present upon resumption to work following my maternity leave?</span><br>
					<span>You need to secure a Post-partum Fit to work certificate issued by attending OBGyne specialist and you have accomplished the MAT2 requirements.</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>What happen if I have not filed my MAT2 requirement yet and decides to resign from work after giving birth?</span><br>
					<span>You are required to return to the company the amount of first half benefit that was previously provided to you. Once done, COMPBEN team will return to you the MAT1 documents and issues a Certificate of No Advance payment and a copy of L501. Consequently, you need to process your MAT1 and MAT2 papers directly to SSS main office as a voluntary member.</span><br><br>

					<h4 style="color: #2777da;"><b>SSS SALARY LOAN</b></h4><br>
					<span style='font-size:13px; font-weight:bold; '>How long will it take the member to pay back the loan?</span>
					<br>
					<span>The one-month or two-month salary loan shall be payable within two (2) years in 24 equal monthly installments.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>When can a member renew a salary loan with the SSS?</span>
					<br>
					<span>The loan may be renewed after the prescribed amortization period of two (2) years. Balance of P500 or less shall be deducted from the proceeds of the new loan.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>How does a member pay back the loan?</span><br>
					<span>For employed member-borrower (including participants of the fast-track program), the first deduction from their salaries shall start on the second month following the date of loan. All remittances shall be made on or before the 10th day following the applicable month of deduction to the SSS or to any of its authorized banks.</span><br>
					<span>The employee is obliged to report to the new employer his obligations with SSS and shall allow the new employer to deduct from his salary the corresponding amortizations due, plus any penalty for late remittance of amortizations</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>In case of transfer, separation, or resignation, how can a borrower pay back the loan?</span>
					<br>
					<span>In case a member-borrower is separated voluntarily, (e.g. retirement or resignation) or involuntarily, (e.g. termination of employment or cessation of operations of the company), the employer shall be required to deduct the total balance of the loan from any benefit(s) due to the employee and shall remit the same in full to SSS. If the benefit(s) due the employee or the amount thereof legally available for offset of obligations of the employee is insufficient to fully repay the loan, the employer shall report theunpaid loan balance to SSS.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>In case of member-borrower’s death, total disability or retirement, what happens to the unpaid loan?</span>
					<br>
					<span>In case of members' death, total disability or retirement under the Social Security Act, the entire amount or any unpaid amount of the loan as well as the interest and penalty thereon, If any,shall be deducted from the corresponding benefit.</span><br><br>

					<h4 style="color: #2777da;"><b>PAG-IBIG</b></h4><br>
					<span style='font-size:13px; font-weight:bold; '>What is the Pag-IBIG Fund Multi-Purpose Loan Program?</span>
					<br>
					<span>The Pag-IBIG Fund Multi-Purpose Loan or MPL Program is a cash loan that qualified members may secure to assist them with any immediate financial need. Here are a few examples where you can use your MPL :</span><br>
					<span>*Minor home improvement</span><br>
					<span>*Additional capital for a small business</span><br>
					<span>*Tuition Fee and other education-related expenses</span><br>
					<span>*Purchase of furniture, appliances or electronic gadgets</span><br>
					<span>*Payment of utility and credit card bills</span><br>
					<span>*Vacation and travel</span><br>
					<span>*Special events</span><br>
					<span>*Car Repair; or</span><br>
					<span>*Other Needs</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>How can I qualify?</span>
					<br>
					<span>You must be an actively contributing member with at least twenty-four (24) monthly membership savings (MS) and sufficient proof of income to qualify. If you have If an existing Pag-IBIG Fund Housing Loan, MPL and/or Calamity Loan, your payments must be updated for you to qualify.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>How much may I borrow?</span><br>
					<span>With Pag-IBIG Fund’s MPL program, qualified members can borrow up to 80% of their total Pag-IBIG Regular Savings, which consist of their monthly contributions, their employer’s contributions, and accumulated dividends earned. If you have an outstanding Calamity Loan, the amount of loan you will receive shall be the difference between the 80% of your total Pag-IBIG Regular Savings and the outstanding balance of your Calamity Loan.</span><br>
					<span>This means that the more you save in your Pag-IBIG Regular Savings, the higher loan amount you may apply for via the MPL.</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>How do I pay my MPL?</span><br>
					<span>The loan is payable within 24 months and comes with a deferred first payment. Formally-employed members shall pay their loan amortization through a salary deduction arrangement with their employer.</span><br><br>

					<span style='font-size:13px; font-weight:bold;'>When can I renew my MPL?</span><br>
					<span>You may renew your MPL after paying an equivalent of six (6) monthly amortizations and upon satisfying the qualifications mentioned above. The outstanding balance of your existing loan shall be deducted from the loan proceeds of your new loan.</span><br><br>

					<h4 style="color: #2777da;"><b>Philhealth</b></h4><br>

					<span style='font-size:13px; font-weight:bold; '>Why Do You Need PhilHealth?</span><br>
					<span>The benefits of enrolling in PhilHealth encompasses inpatients and outpatients, senior citizens, dual citizens and foreigners, premature and small newborns, and pregnant women. But it’s more than that—being a PhilHealth contributor allows you to enjoy special benefits paid to the accredited Health Care Institution through all case rates.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>Who are qualified as PhilHealth dependents?</span><br>

					<span>The following people can be registered as a PhilHealth dependent:</span><br>
					<span>*Legitimate spouse (non-member)</span><br>
					<span>*Child or Children (legitimate, legitimated, acknowledged and illegitimate)</span><br>
					<span>*Child or Children (adopted or stepchild or stepchildren below 21 years of age that are unmarried and unemployed</span><br>
					<span>*Children (21) years old or above but suffering from congenital disability, either physical or mental, or any disability acquired that renders them totally dependent on the member for support, as determined by the Corporation;</span><br>
					<span>*Foster child as defined in Republic Act 10165 otherwise known as the Foster Care Act of 2012;</span><br>
					<span>*Parents who are (60) years old or above (non-member), whose monthly income is below an amount to be determined by PhilHealth in accordance with the guiding principles set forth in the NHI Act of 2013</span><br>
					<span>*Parents with a permanent disability regardless of age as determined by PhilHealth, that renders them 100% dependent on the member for support.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>Where to get the CSF and MDR?</span><br>
					<span>Kindly approach any nurse on duty for membership verification prior to the issuance of Claim Signature Form (CSF). On the other hand, please visit your nearest Philhealth office for the procurement and updating of Member Data Record (MDR). For the complete list of PhilHealth branches, please visit https://www.philhealth.gov.ph/about_us/map/regional.htm.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>How do I claim PhilHealth benefits?</span><br>

					<span>Prepare and complete all the necessary documents for filing a claim. You can get copies from the hospital or download the appropriate forms from the Philhealth website.</span><br>
					<span>*Member Data Record Form (MDR)</span><br>
					<span>*PhilHealth claim form 1 (original copy)</span><br>
					<span>*Certificate of payments (with OR numbers)</span><br>
					<span>*PhilHealth ID and another valid ID</span><br><br>

