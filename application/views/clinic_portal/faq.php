					<span>Having your own PhilHealth Insurance ID Card guarantees you:</span><br>
					<span>*15% discount on generic drugs on Watson’s, Rose Pharmacy, South Star Drug, and The Generics Pharmacy</span><br>
					<span>*20% to 80% discount on flu vaccines on PQ Health Shield and GSK</span><br>
					<span>*Free eye exam from Vivian Sarabia Optical</span><br>
					<span>*20% discount on regular items in Vivian Sarabia Optical</span><br>
					<span>*10% discount on drug testing services at JNW Drug Testing sites</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>Does Philhealth covers dental care?</span><br>
					<span>Yes. Philhealth covers dental care but only those procedures done in hospitals and health Maintenace organizations (HMO). This means only those that are considered of surgical type and do not include cleaning and tooth extraction.</span><br><br>

