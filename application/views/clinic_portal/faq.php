					<span>1. Text 0917-5129149 or send an e-mail to PhilHealth to ofp@philhealth.gov.ph. Include the following details in this format:</span><br>
					<span>LAST NAME</span><br>
					<span>FIRST NAME</span><br>
					<span>MIDDLE NAME</span><br>
					<span>BIRTHDAY</span><br>
					<span>2. Contact PhilHealth branches near you or the head office.</span><br>
					<span>3. Check with your previous employer if they still have your PhilHealth records.</span><br><br>

					<span style='font-size:13px; font-weight:bold; '>How to Get your PhilHealth Card?</span><br>
					<span>PhilHealth Insurance ID Cards are granted to both the formal economy and informal sector. You may claim it for P90 at any PhilHealth office. But if you wish to declare a dependent for your ID card, you need to go to any Bayad Center outlet and pay P358. Two (2) valid IDs of your dependent are required to proceed.</span><br><br>

