<script type="text/javascript">

$(document).ready(function(){
	
	$('.play_video').click(function(e){
		var vurl=$(this).attr("vurl");
		var title=$(this).find('.video-title').text();
		
		
		$("#video_model").modal('show');
		$("#video_model").find('.modal-title').text(title);
		
		$('#video_controls source').attr('src', vurl)
		$('#video_controls')[0].load();
		var vid = document.getElementById("video_controls");
		vid.muted = false;
			
	});
	
	
	$('#video_model').on('hidden.bs.modal', function () {
		var vid = document.getElementById("video_controls");
		//vid.muted = true;
		vid.pause();
    });
	
	
	
});

